def get_hcode_mapping_markdown() -> str:
    """Return H-code mapping as a Markdown table for MCP Resource."""
    lines = [
        "# GHS Hazard Category → H-code Mapping Table",
        "",
        "| Hazard Category | H-code |",
        "|---|---|",
    ]
    for category, hcode in HAZARD_TO_HCODE.items():
        lines.append(f"| {category} | {hcode} |")
    return "\n".join(lines)


def get_hcode_mapping_json() -> dict[str, str]:
    """Return the full mapping dict."""
    return dict(HAZARD_TO_HCODE)
